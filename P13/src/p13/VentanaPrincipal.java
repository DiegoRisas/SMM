    private void seleccionEspacioCOLORActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_seleccionEspacioCOLORActionPerformed
        VentanaInterna vi = (VentanaInterna) (escritorio.getSelectedFrame());
        if (vi != null) {
            BufferedImage img = vi.getLienzo2D().getImg();
            if(img!=null){
                ColorSpace cs = null;
                int seleccion = this.seleccionEspacioCOLOR.getSelectedIndex();
                BufferedImage imgdest = null; 
                String nombreEspacioColor = null;
                
                try {
                    // Determinar el espacio de color seleccionado
                    switch(seleccion) {
                        case 0: // sRGB
                            cs = ColorSpace.getInstance(ColorSpace.CS_sRGB);
                            nombreEspacioColor = "sRGB";
                            break;
                        case 1: // YCC
                            cs = ColorSpace.getInstance(ColorSpace.CS_PYCC);
                            nombreEspacioColor = "YCC";
                            break;
                        case 2: // GREY
                            cs = ColorSpace.getInstance(ColorSpace.CS_GRAY);
                            nombreEspacioColor = "GREY";
                            break;
                        default:
                            break;
                    }

                    // Verificar si es necesario realizar la conversin
                    if (cs != null && !img.getColorModel().getColorSpace().equals(cs)) {
                        ColorConvertOp op = new ColorConvertOp(cs, null);
                        imgdest = op.filter(img, null);
                    } else {
                        // No es necesario realizar la conversin, mantener la imagen original
                        imgdest = img;
                    }

                    if (imgdest != null) {                      
                        // Mostrar la nueva imagen en una ventana interna
                        VentanaInterna nuevaVi = new VentanaInterna(vi);
                        nuevaVi.setTitle(vi.getTitle()+ "(" + nombreEspacioColor +")" );
                        nuevaVi.getLienzo2D().setImg(imgdest);
                        escritorio.add(nuevaVi);
                        nuevaVi.setVisible(true);
                    }
                } catch(IllegalArgumentException e){
                    System.err.println(e.getLocalizedMessage());
                }
            }
        }    
    }//GEN-LAST:event_seleccionEspacioCOLORActionPerformed

