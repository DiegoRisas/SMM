    private void posicionVentana(JDesktopPane escritorio) {
        JInternalFrame[] allFrames = escritorio.getAllFrames();
        int x = 0, y = 0;
        
        if(allFrames.length > 0){
            JInternalFrame ultimoFrame = allFrames[0];
            x = ultimoFrame.getX() + xOffset;
            y = ultimoFrame.getY() + yOffset;
        }
        
        if(x + this.getWidth() > escritorio.getWidth()){
            x = 0;
        }
        
        if( y + this.getHeight() > escritorio.getHeight()){
            y = 0;
        }
        
        this.setLocation(x, y);
        //System.out.println("p11.VentanaInterna.posicionVentana()  " + allFrames.length );
    }
    
    
    private JDesktopPane findDesktopPane(javax.swing.JInternalFrame frame) {
        // Get the parent of the frame
        javax.swing.JComponent parent = (javax.swing.JComponent) SwingUtilities.getAncestorOfClass(javax.swing.JDesktopPane.class, frame);
        // Check if the parent is a JDesktopPane
        if (parent instanceof javax.swing.JDesktopPane) {
            return (javax.swing.JDesktopPane) parent;
        }
        // If not found, return null
        return null;
    }
