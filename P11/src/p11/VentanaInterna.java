    public VentanaInterna(VentanaInterna otra){
        this();
        this.setTitle(otra.getTitle() + " (Copia)");
        
        // Clona el lienzo de la ventana interna original y configura el lienzo de la nueva ventana interna
        PanelLienzo lienzoOriginal = otra.getLienzo2D();
        PanelLienzo lienzoNuevo = new PanelLienzo();
        lienzoNuevo.setImg(lienzoOriginal.getImg()); // Clona la imagen del lienzo original
        this.lienzo = lienzoNuevo;

        // Agrega el nuevo lienzo al panel de la nueva ventana interna
        jScrollPane1.setViewportView(lienzoNuevo);

        // Establece el tamaño predeterminado de la ventana interna
        this.setSize(otra.getWidth(), otra.getHeight());     
        JDesktopPane escritorio = findDesktopPane(otra);
        if (escritorio != null) {
            posicionVentana(escritorio);
        }
    }

    
